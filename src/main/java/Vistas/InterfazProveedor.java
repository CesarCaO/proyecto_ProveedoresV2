    public void actualizarTabla(List<Proveedor> listaProveedores){
        DefaultTableModel modeloTabla = (DefaultTableModel) tblRegistros.getModel();
        modeloTabla.setRowCount(0);
        mensajeConsola("Actualizando tabla");
        for (Proveedor proveedor : listaProveedores) {
            int identificador = proveedor.getIdProveedor();
            String empresa = proveedor.getEmpresa();
            String nombre = proveedor.getNombreContacto();
            String direccion = proveedor.getDireccion();
            String telefono = proveedor.getTel();
            String correo = proveedor.getCorreo();
            String coordenadas = proveedor.getCoordenadas();
            String ciudad = proveedor.getCiudad();
            String estado = proveedor.getEstado();
            Object[] fila = {identificador,empresa,nombre,direccion,telefono,correo,coordenadas,ciudad,estado};
            modeloTabla.addRow(fila);
        }
        tblRegistros.setModel(modeloTabla);
    }
