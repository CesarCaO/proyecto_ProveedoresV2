    private void tbdFuncionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbdFuncionesMouseClicked
        // TODO add your handling code here:
        // TODO add your handling code here:
        
        int filaSeleccionada = tblRegistros.getSelectedRow();
        switch (tbdFunciones.getSelectedIndex()) {
            case 0:
                Insertar(true);
                Editar(false);
                Eliminar(false);
                this.limpiarCampos();
                tblRegistros.clearSelection();
                break;
            case 1:
                if (filaSeleccionada < 0) {
                    mensajeError("Registro no seleccionado", "Seleccione un registro de la tabla para acceder a esta funcion...");
                    tbdFunciones.setSelectedIndex(0);
                    break;
                }
                Insertar(false);
                Editar(true);
                Eliminar(false);
                break;
            case 2:
                if (filaSeleccionada < 0) {
                    mensajeError("Registro no seleccionado", "Seleccione un registro de la tabla para acceder a esta funcion...");
                    tbdFunciones.setSelectedIndex(0);
                    break;
                }
                Insertar(false);
                Editar(false);
                Eliminar(true);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_tbdFuncionesMouseClicked

